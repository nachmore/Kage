//! Single registration point for every `#[tauri::command]`.
//!
//! Lives in the shared `commands` module (not main.rs) so BOTH the
//! production binary and the mock-app test harness
//! (`tests/mock_app_test.rs`) dispatch through the exact same handler —
//! a command registered here is automatically covered by the harness's
//! full-sweep test, and one missing here is not callable in production
//! either (parity by construction, not by convention).
//!
//! Reminder from CLAUDE.md: every public command must be added here or
//! it won't be callable from the frontend.

